  void signUserIn(BuildContext context) {
    // Simulate a delay to mimic login processing (Optional)
    Future.delayed(const Duration(seconds: 1), () {
      // Navigate to ShopPage
      Navigator.pushReplacementNamed(context, '/shop_page');
    });
  }

